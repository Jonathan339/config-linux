-- Configuración para vim-gitgutter
vim.g.gitgutter_enabled = 1          -- Habilita el plugin
vim.g.gitgutter_highlight_lines = 1   -- Resalta las líneas modificadas en el buffer
vim.g.gitgutter_signs = true          -- Muestra los signos en el gutter
vim.g.gitgutter_signs_added = '+'     -- Símbolo para las líneas agregadas
vim.g.gitgutter_signs_modified = '~'  -- Símbolo para las líneas modificadas
vim.g.gitgutter_signs_removed = '-'   -- Símbolo para las líneas eliminadas
vim.g.gitgutter_sign_priority = 6     -- Prioridad de los signos
vim.g.gitgutter_quickfix_enabled = 1  -- Habilita la integración con quickfix
vim.g.gitgutter_preview_diff = 1      -- Habilita la vista previa del diff en el buffer

